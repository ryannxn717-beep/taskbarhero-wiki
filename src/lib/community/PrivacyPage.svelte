  <article class="privacy-article">
    <p>This independent community wiki works without an account server. This policy explains how the site handles visitor data.</p>

    <section>
      <h2>What this site is</h2>
      <p>This is an independent fan-made game wiki. It is not operated by the game developer and does not provide a shared community account.</p>
    </section>

    <section>
      <h2>Analytics</h2>
      <p>The site does not send analytics, behavioral events, page histories, device identifiers or advertising measurements to a remote service.</p>
    </section>

    <section>
      <h2>Local storage</h2>
      <p>Demo sign-in state, community drafts and votes are saved in your browser's local storage. They remain on this device until you clear site data. They are never published to other visitors.</p>
    </section>

    <section>
      <h2>External links</h2>
      <p>Links to Discord or other external services leave this site. Those services apply their own privacy policies. No credentials are collected by this site.</p>
    </section>

    <section>
      <h2>Advertising</h2>
      <p>No advertising network, tracking pixel or personalized advertising system is connected. The “Remove ads” control only hides the related interface element.</p>
    </section>

    <section>
      <h2>Game data and disclaimer</h2>
      <p>Game names and images are presented for fan reference. The data may be incomplete or out of date and should not be treated as an official source.</p>
    </section>

    <section>
      <h2>Contact</h2>
      <p>Because this build runs locally and has no operator inbox, there is no remote support or deletion request channel. Clearing browser storage removes all locally created community state.</p>
    </section>
